// Get manufacturer by name
function getManufacturer(manufacturerName) {
	for (var i = 0; i < AFRL.manufacturers.length; i++) {
		if (AFRL.manufacturers[i].name === manufacturerName) {
			return AFRL.manufacturers[i];
		}
	}
}

// Get manufacturer user by name
function getUser(currentUser) {
	for (var i = 0; i < AFRL.manufacturers.manufacturerUsers.length; i++) {
		if (AFRL.manufacturers.manufacturerUsers[i].name === currentUser) {
			return AFRL.manufacturers.users[i];
		}
	}
}
